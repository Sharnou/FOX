// Get locale config for a country code
export function getLocale(countryCode) {
  const config = COUNTRY_CONFIG[countryCode];
  if (!config) return { lang: 'en', dir: 'ltr', currency: 'USD', name: countryCode };
  return config;
}

// Get translations for a language
export function getT(lang) {
  return TRANSLATIONS[lang] || TRANSLATIONS.en;
}

// Detect country and set locale (client-side)
export async function detectAndSetLocale() {
  if (typeof window === 'undefined') return { lang: 'ar', dir: 'rtl', country: 'EG' };
  
  // Check if already set
  const stored = localStorage.getItem('country');
  const storedLang = localStorage.getItem('lang');
  if (stored && storedLang) {
    return { ...getLocale(stored), country: stored };
  }

  // Detect from IP
  try {
    const res = await fetch('https://ipapi.co/json/');
    const data = await res.json();
    const country = data.country_code || 'EG';
    const locale = getLocale(country);
    
    localStorage.setItem('country', country);
    // Auto-save location for nearby features
    if (data.latitude) localStorage.setItem('userLat', data.latitude);
    if (data.longitude) localStorage.setItem('userLng', data.longitude);
    localStorage.setItem('lang', locale.lang);
    localStorage.setItem('dir', locale.dir);
    localStorage.setItem('currency', locale.currency);
    
    return { ...locale, country };
  } catch {
    // Default to Arabic (Egypt) if detection fails
    return { lang: 'ar', dir: 'rtl', currency: 'EGP', country: 'EG' };
  }
}
