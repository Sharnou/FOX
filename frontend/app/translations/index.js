/**
 * Get a translations dict for the given language.
 * Falls back to Arabic if language not found.
 */
export function getTranslations(lang) {
  return translations[lang] || translations['ar'];
}

/**
 * Create a t() function for the given language.
 * Fallback chain: lang → ar → key name
 */
export function makeT(lang) {
  const dict = translations[lang] || translations['ar'];
  const ar = translations['ar'];
  return function t(key) {
    return dict[key] !== undefined ? dict[key]
      : ar[key] !== undefined ? ar[key]
      : key;
  };
}

